package org.sunbird.utils;

/**
 * Interface defining various notification type constants used across the application.
 */
public interface NotificationType {

  /** Type for member update notifications. */
  String MEMBER_UPDATE = "member-update";

  /** Type for group deletion notifications. */
  String GROUP_DELETE = "group-delete";

  /** Type for activity update notifications. */
  String ACTIVITY_UPDATE = "activity-update";
}
